"""Build a rigged, animated exercise figure and export GLB + thumbnail.

Runs headless inside Blender in CI (no GPU needed):

    blender -b -noaudio --python-exit-code 1 -P pipeline/build_exercise.py -- exercises/squat.json site/assets

v0.4: the figure is a real anatomical human generated by MPFB2 (MakeHuman
for Blender) — male, muscle 0.9 — rigged with the canonical MakeHuman
skeleton. Poses stay in the same exercise JSONs: a hidden synthetic
"driver" rig evaluates them, then each MakeHuman bone is aimed at the
driver bone's world direction (shortest arc), so the retarget is
independent of the two rigs' different rest poses (MPFB rests in A-pose).
Active muscle regions are painted onto the body via MakeHuman's own
skin-weight territories. Writes <id>.glb and <id>.png (mid-rep frame).
"""

